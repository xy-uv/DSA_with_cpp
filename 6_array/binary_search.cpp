int binarySearch(int array[],int n,int k){
    int s=0;
    int e=n-1;
    int m=s+(e-s)/2;
    while(s<=e){
        if(k==array[m]){
            return m;
        }

        if(k>array[m]){
            s=m+1;
        }else{
            e=m-1;
        }
        m=s+(e-s)/2;
    }
    return -1;
}

